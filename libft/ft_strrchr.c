char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
		i++;
	if (c == 0)
		return ((void *)(s + i));
	i = i - 1;
	while (i >= 0)
	{
		if (s[i] == c)
			return ((void *)(s + i));
		else
			i--;
	}
	return (0);
}
